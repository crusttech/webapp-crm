<template>
  <div class="py-3">
    <b-container fluid>
      <b-row no-gutters>
        <b-col xl="8" offset-xl="2">
          <b-card :title="$t('page.title')"
                  no-body
                  class="shadow-sm"
          >
            <b-card-header header-bg-variant="white"
                           class="py-3"
            >
              <h1 class="mb-3" data-v-onboarding="page-list">
                {{ $t('page.title') }}
              </h1>
              <b-row
                no-gutters
              >
                <b-col xl="4" lg="5" md="7">
                  <b-form @submit.prevent="handleAddPageFormSubmit" class="mr-1">
                    <b-form-group v-if="namespace.canCreatePage" class="mb-1">
                      <b-input-group>
                        <b-input required type="text" v-model="page.title" class="page-name-input" id="name" :placeholder="$t('page.newPlaceholder')" />
                        <b-input-group-append>
                          <b-button type="submit" variant="primary" size="lg" data-v-onboarding="create-page">{{ $t('page.createLabel') }}</b-button>
                        </b-input-group-append>
                      </b-input-group>
                    </b-form-group>
                  </b-form>
                </b-col>
                <b-col xl="8" lg="7" md="5">
                  <c-permissions-button
                    v-if="namespace.canGrant"
                    resource="compose:page:*"
                    class="btn-lg"
                    :buttonLabel="$t('general.label.permissions')"
                    buttonVariant="light"
                    data-v-onboarding="permissions"
                  />
                </b-col>
              </b-row>
            </b-card-header>
            <b-row class="pages-list-header border-top align-content-center" no-gutters>
              <b-col
                cols="12"
                class="pl-4"
              >
                <span class="font-weight-bold">
                  {{ $t('page.newPlaceholder') }}
                </span>
                <span class="text-muted font-italic ml-3">
                  {{ $t('page.instructions') }}
                </span>
              </b-col>
            </b-row>
            <page-tree
              :namespace="namespace"
              class="pb-2"
              @reorder="handleReorder"
              v-model="tree"/>
          </b-card>
        </b-col>
      </b-row>
    </b-container>
    <tour name="PageList" ref="tour" />
  </div>
</template>

<script>
import { mapActions } from 'vuex'
import PageTree from 'corteza-webapp-compose/src/components/Admin/Page/Tree'
import { compose } from '@cortezaproject/corteza-js'
import Tour from 'corteza-webapp-compose/src/components/Tour/Tour'

export default {
  name: 'PageList',

  components: {
    PageTree,
    Tour,
  },

  props: {
    namespace: {
      type: compose.Namespace,
      required: false,
    },
  },

  data () {
    return {
      tree: [],
      page: new compose.Page({ visible: true }),
    }
  },

  created () {
    this.loadTree()
  },
  mounted () {
    this.$refs.tour.start()
  },
  methods: {
    ...mapActions({
      createPage: 'page/create',
    }),

    loadTree () {
      const { namespaceID } = this.namespace
      this.$ComposeAPI.pageTree({ namespaceID }).then((tree) => {
        this.tree = tree.map(p => new compose.Page(p))
      }).catch(this.toastErrorHandler(this.$t('notification.page.loadFailed')))
    },

    handleAddPageFormSubmit () {
      const { namespaceID } = this.namespace
      this.page.weight = this.tree.length
      this.createPage({ ...this.page, namespaceID }).then(({ pageID }) => {
        this.$router.push({ name: 'admin.pages.edit', params: { pageID } })
      }).catch(this.toastErrorHandler(this.$t('notification.page.saveFailed')))
    },

    handleReorder () {
      this.loadTree()
    },
  },
}
</script>
